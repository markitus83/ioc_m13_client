    private void btnTancarSessioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTancarSessioActionPerformed
        DTOUsuari usuari = new DTOUsuari();
                        
        try {
            JSONObject responseLogin = usuari.logoutUsuari(codiAcces);
            
            if (200 != responseLogin.getInt("code")) {
                new SimpleDialog().errorMessage(responseLogin.getString("message"));
            } else {
                new SimpleDialog().infoMessage(responseLogin.getString("message"));
                new LoginView().setVisible(true);
                this.dispose();
            }
        } catch (IOException ex) {
            Logger.getLogger(LoginView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTancarSessioActionPerformed

